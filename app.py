# -*- coding: utf-8 -*-
import streamlit as st
import pandas as pd
import plotly.express as px

# ================================
# LOAD CSS THEME
# ================================
with open("sabdatani.css") as f:
    st.markdown(f"<style>{f.read()}</style>", unsafe_allow_html=True)

st.set_page_config(page_title="Dashboard Sabda Tani â€“ Gapoktan", layout="wide")

# ================================
# LOAD DATA
# ================================
# data1 = kecamatanâ€“desa
# data2 = desaâ€“gapoktan
data1 = pd.read_excel("data1.xlsx")
data2 = pd.read_excel("data2.xlsx")

# ================================
# BERSIHKAN DATA
# ================================
data1["kecamatan"] = data1["kecamatan"].astype(str).str.strip()
data1["desa"] = data1["desa"].astype(str).str.strip()

data2["desa"] = data2["desa"].astype(str).str.strip()
data2["gapoktan"] = data2["gapoktan"].astype(str).str.strip()

# ================================
# MERGE untuk hasil lengkap kecamatanâ€“desaâ€“gapoktan
# ================================
merged = data1.merge(data2, on="desa", how="left")

# ================================
# BANGUN HIERARKI:
# Kecamatan â†’ Desa â†’ Gapoktan
# ================================
data_hierarchy = {}

for _, row in merged.iterrows():
    kec = row["kecamatan"]
    desa = row["desa"]
    gap = row["gapoktan"]

    if kec not in data_hierarchy:
        data_hierarchy[kec] = {}

    if desa not in data_hierarchy[kec]:
        data_hierarchy[kec][desa] = []

    if pd.notna(gap):
        data_hierarchy[kec][desa].append(gap)

# ================================
# LIST KECAMATAN BERSIH
# ================================
list_kecamatan_clean = sorted(list(data_hierarchy.keys()))

# ================================
# DASHBOARD HEADER
# ================================
st.markdown("""
<div class="title">
ðŸŒ¾ <span>Dashboard Sabda Tani â€“ Gapoktan</span>
</div>
""", unsafe_allow_html=True)


# ================================
# HITUNG KPI
# ================================
total_kec = len(data_hierarchy)

total_desa = merged["desa"].nunique()

total_gapoktan = merged["gapoktan"].nunique()

col1, col2, col3 = st.columns(3)

col1.markdown(f"""
<div class="card">
<h3>Total Kecamatan</h3>
<p>{total_kec}</p>
</div>
""", unsafe_allow_html=True)

col2.markdown(f"""
<div class="card">
<h3>Total Desa</h3>
<p>{total_desa}</p>
</div>
""", unsafe_allow_html=True)

col3.markdown(f"""
<div class="card">
<h3>Total Gapoktan</h3>
<p>{total_gapoktan}</p>
</div>
""", unsafe_allow_html=True)

st.markdown("<br>", unsafe_allow_html=True)


# ================================
# GRAFIK GAPOKTAN PER KECAMATAN
# ================================
st.markdown("### ðŸ“Š Jumlah Gapoktan per Kecamatan")

chart_kec = []

for kec in data_hierarchy:
    total_g = sum(len(g_list) for g_list in data_hierarchy[kec].values())
    chart_kec.append({"kecamatan": kec, "gapoktan": total_g})

df_chart = pd.DataFrame(chart_kec)

fig = px.bar(
    df_chart,
    x="kecamatan",
    y="gapoktan",
    color="gapoktan",
    color_continuous_scale="Greens",
    title="Distribusi Gapoktan per Kecamatan"
)
st.plotly_chart(fig, use_container_width=True)


# ================================
# MENU INTERAKTIF
# ================================
st.markdown("### ðŸ”Ž Detail Gapoktan per Kecamatan & Desa")

c1, c2 = st.columns(2)

# Pilih kecamatan
kec_select = c1.selectbox(
    "Pilih Kecamatan",
    list_kecamatan_clean
)

# Pilih desa sesuai kecamatan
desa_list = sorted(list(data_hierarchy[kec_select].keys()))

desa_select = c2.selectbox(
    "Pilih Desa",
    desa_list
)

# Ambil gapoktan
gapoktan_list = data_hierarchy[kec_select][desa_select]

st.markdown(f"### ðŸŒ± Daftar Gapoktan â€“ **{desa_select}**")

if len(gapoktan_list) == 0:
    st.info("Tidak ada data gapoktan untuk desa ini.")
else:
    for g in gapoktan_list:
        st.markdown(f"- {g}")

